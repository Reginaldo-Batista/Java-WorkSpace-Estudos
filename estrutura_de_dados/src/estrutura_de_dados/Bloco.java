package estrutura_de_dados;

public class Bloco {

	private int valor;
	private Bloco proximo;

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	public Bloco getProximo() {
		return proximo;
	}

	public void setProximo(Bloco proximo) {
		this.proximo = proximo;
	}
}
